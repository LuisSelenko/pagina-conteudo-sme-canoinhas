        <div id="text-header"><img src="logo.jpg" alt="Logo Canoinhas" ><h1>PREZADOS FAMILIARES E ALUNOS</h1></div>
        <p align="justify">O Governo do Município de Canoinhas suspendeu temporariamente as aulas na Rede Pública Municipal por meio do Decreto nº 058 de 17 de março de 2020, amparado nos Decretos do Governo Estadual nº 509, de 17 de março de 2020 e Decreto nº 525 de 25 de março de 2020, em virtude dos crescentes casos do Novo Coronavírus (COVID-19) no Brasil e procurando atender às medidas de prevenção e combate ao contágio para preservar a saúde.</p>
        <p align="justify">A secretaria Municipal de Educação de Canoinhas com o objetivo de fornecer alternativas de estudo para orientar os pais e atender os estudantes durante o período de distanciamento social, apresenta algumas propostas para complementar o ensino aprendizagem.</p>
        <p align="justify">É muito importante a participação dos pais e responsáveis nesse momento, fazendo atividades e auxiliando os filhos. Algumas dicas:</p>
        <ul>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Organizar uma rotina para os filhos estabelecendo horários para dormir, alimentação, banho, atividades pedagógicas, brincadeiras e jogos;</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Priorizar um espaço para estudo;</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Realizar atividades lúdicas, educativas, prazerosas e criativas junto aos filhos, como: atividades de imaginação, criação de brinquedos de sucata, desenhar, escrever cartinhas, montar quebra-cabeça, inventar o próprio jogo de tabuleiro, criar roupas para os bonecos, massinha de modelar, colecionar figurinhas, jogos de adivinhação, brincadeiras de luz e sombra, sessão de cinema em casa, caça ao tesouro, cuidar de plantas.</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Auxiliar e realizar com os filhos atividades pedagógicas de leitura: ler histórias, estimular a leitura dos filhos, promover leituras em voz alta, fazendo a interpretação;</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Incentivar a escrita: fazer listas de objetos que estão em cada ambiente da casa; incentivar a leitura de livros; preencher cruzadinhas e caça palavras; atividades de pintura com tinta; preparar algo gostoso na cozinha, seguindo uma receita (incentiva a leitura, a relação e quantidade, a comparação de diferentes tipos de medidas...)</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Realizar atividades de contagem práticas a partir da realidade do aluno;</li></div>
          <div id="list-item"><i class="fas fa-arrow-right"></i><li>Utilizar a internet para jogos pedagógicos e atividades educativas.</li></div>
        </ul>
        <p align="justify">A Secretaria Municipal de Educação está disponibilizando uma série de atividades, jogos e exercícios para auxiliar nas aprendizagens dos alunos nesse período da quarentena.</p>
